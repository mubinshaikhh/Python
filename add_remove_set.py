# Initialize a set
my_set = {1, 2, 3, 4, 5}

# Display the initial set
print("Initial Set:", my_set)

# Adding items to the set
my_set.add(6)
my_set.update({7, 8, 9})

# Display the set after adding items
print("Set after adding items:", my_set)

# Removing items from the set
my_set.remove(3)
my_set.discard(8)

# Display the set after removing items
print("Set after removing items:", my_set)
